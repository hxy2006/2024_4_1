public class VarTest{
    public static void main(String[] args){
        //声明一个int变量起名为age
        int age;

        //给age赋值
        age = 20;

        //读取数据
        System.out.println("年龄=" + age);

        //修改变量值
        age = 30;

        //再次读取数据
        System.out.println("年龄=" + age);

    }
}